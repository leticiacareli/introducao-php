<?php
//aula 8 - incremento/decremento
echo nl2br("\n");

$number = 10;

echo ++$number; //pré-incremento
echo nl2br("\n");
echo $number++; //pós-incremento
echo nl2br("\n" . $number);

echo nl2br("\n");

echo --$number; //pré-decremento
echo nl2br("\n");
echo $number--; //pós-decremento
echo nl2br("\n" . $number);